using Microsoft.AspNetCore.Components;

namespace SpenSoft.DanBeamNG.Components;

public partial class Input_Select : ComponentBase
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Constructors
    #endregion
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Parameters

    [Parameter]
    public List<KeyValuePair<Guid, String>>? ValueList { get; set; }

    [Parameter]
    public String? Label { get; set; }

    [Parameter]
    public Guid? BindingProperty { get { return SelectedStatus; } set { SelectedStatus = value; } }

    [Parameter]
    public EventCallback<Guid?> StatusCodeCallback { get; set; }

    [Parameter]
    public String? Placeholder { get; set; } = "Enter";

    [Parameter]
    public String? LabelWidth { get; set; } = "40%";

    [Parameter]
    public String? InputWidth { get; set; } = "60%";


    #endregion Parameters
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Injections
    #endregion Injections
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Public Properties

    private Guid? _SelectedStatus { get; set; }
    public Guid? SelectedStatus
    {
        get { return _SelectedStatus; }
        set
        {
            if (_SelectedStatus != value)
            {
                _SelectedStatus = value;
                StatusCodeCallback.InvokeAsync(value);
            }
        }
    }




    #endregion Public Properties
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Screen State Properties
    #endregion Screen State Properties
    ////////////////////////////////////////////////////////////////////////////////////////////////////
}
