using Microsoft.AspNetCore.Components;

namespace SpenSoft.DanBeamNG.Components;

public partial class Input_File : ComponentBase
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Constructors
    #endregion
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Parameters
    [Parameter]
    public String? Label { get; set; }

    [Parameter]
    public String? LabelWidth { get; set; } = "40%";

    [Parameter]
    public String? InputWidth { get; set; } = "60%";

    [Parameter]
    public String? BindingProperty { get { return FileName; } set { FileName = value; } }

    [Parameter]
    public EventCallback<String?> FileSelectedCallback { get; set; }


    #endregion Parameters
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Injections
    #endregion Injections
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Public Properties

    private String? _FileName { get; set; }
    public String? FileName
    {
        get { return _FileName; }
        set
        {
            if (_FileName != value)
            {
                _FileName = value;
                FileSelectedCallback.InvokeAsync(value);
            }
        }
    }

    #endregion Public Properties
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Screen State Properties
    #endregion Screen State Properties
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Override Methods
    #endregion Override Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Protected Methods
    #endregion
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Public Methods
    #endregion
    ////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////
    #region Private Methods
    #endregion
    ////////////////////////////////////////////////////////////////////////////////////////////////////
}
